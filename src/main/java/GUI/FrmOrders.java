    public void showData() throws SQLException {
        
        //Remove row update (Repasar)
        while(model.getRowCount()>0){
            model.removeRow(0);
        }
        
        //Select Method
        List<Order> orders = orderDao.select();
        Object[] object = new Object[5];
        for (int i = 0; i < orders.size(); i++) {
            object[0] = orders.get(i).getId();
            object[1] = orders.get(i).getClient();
            object[2] = orders.get(i).getQuantity();
            object[3] = orders.get(i).getTotal();
            object[4] = orders.get(i).isPay();
            
            if(object[4].toString().equals("true")){
                object[4] = "Is pay";
            }
            
            else{
                object[4] = "Don't pay";
            }
            
            model.addRow(object);
        }

    }
    
