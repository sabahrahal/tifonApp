    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // Insert
        String client = txtClient.getText();
        int quantity  = parseInt(txtQuantity.getText());
        boolean pay2 = false;  
        if(btnPayYes.isSelected()){
            pay2 = true; 
        }
        else{
            pay2 = false; 
        }
        
        order.setClient(client);
        order.setQuantity(quantity);
        order.setPay(pay2);
        
        //show Total
        String total = String.valueOf(order.calculateTotal(quantity));
        txtTotal.setText(total);
        JOptionPane.showMessageDialog(null, "Your order has been successfully registered. " + '\n' +"Order total:: " + total + "$");
        try { 
            orderDao.insert(order);
            showData();
        } catch (SQLException ex) {
           ex.printStackTrace(System.out);
        }
        
        finally{
            CleanAddTextField();
            
        }
        
        
    }//GEN-LAST:event_btnAddActionPerformed

